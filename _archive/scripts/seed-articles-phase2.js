async function seedArticles() {
  console.log('🚀 Начинаем Фазу 2: Загрузка 10 экспертных статей...');
  
  for (const article of articles) {
    const { data, error } = await supabase
      .from('journal_articles')
      .upsert(article, { onConflict: 'slug' });
      
    if (error) {
      console.error(`❌ Ошибка загрузки "${article.slug}":`, error.message);
    } else {
      console.log(`✅ Статья загружена: ${article.title_ru}`);
    }
  }
  
  console.log('🏁 Фаза 2 завершена. Теперь у нас 15 статей!');
}

seedArticles();
