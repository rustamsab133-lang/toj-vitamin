async function updateSEO() {
  console.log('🚀 Starting SEO Optimization for Top 10 Products...');

  for (const item of SEO_PRODUCTS) {
    const { error } = await supabase
      .from('products')
      .update({
        name: item.new_name,
        description: item.seo_description,
        tags: item.tags
      })
      .ilike('name', `%${item.original_name}%`);

    if (error) {
      console.error(`❌ Error updating ${item.original_name}:`, error);
    } else {
      console.log(`✅ Optimized: ${item.new_name}`);
    }
  }

  console.log('✨ SEO Optimization Complete!');
}

updateSEO();
