async function run() {
  const { data: products } = await client.from('products').select('id, name, description');
  
  const descCounts = {};
  products.forEach(p => { descCounts[p.description] = (descCounts[p.description] || 0) + 1; });
  const targets = products.filter(p => descCounts[p.description] > 1);
  
  console.log(`Targeting ${targets.length} products with duplicated descriptions...`);
  let count = 0;
  
  for (let prod of targets) {
    const data = generateUniqueContent(prod);
    
    await client.from('products').update({ 
      description: data.description,
      marketing_hooks: data.hooks
    }).match({ id: prod.id });
    
    console.log(`[✓] ${prod.name}`);
    count++;
  }
  
  console.log(`\n✅ Successfully updated ${count} products with unique descriptions!`);
  
  // Final verification
  const { data: verify } = await client.from('products').select('description');
  const verifyDescs = verify.map(d => d.description);
  const stillDuplicates = verify.filter(d => verifyDescs.indexOf(d.description) !== verifyDescs.lastIndexOf(d.description));
  console.log(`📊 Remaining duplicates: ${stillDuplicates.length}`);
}

